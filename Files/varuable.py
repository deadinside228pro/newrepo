def doOperationsWithNumbers(a, b):
    return ((5 * a) + (b ** 2)) / (b - a)


def calculateAmountOfDepositForOneMonth(summx, procent):
    return summx * (procent / 100) + summx


def decideEquation(a, b, c):
    return (c - b) / a


def reverseString(a, b):
    c = a
    a = b
    b = c
    return a, b
